    private String generatePersonId(String role) {
    // Generate unique person ID based on role
    String prefix;
    switch (role) {
        case "STUDENT":
            prefix = "002";
            break;
        case "FACULTY":
            prefix = "001";
            break;
        case "ADMIN":
            prefix = "000";
            break;
        case "REGISTRAR":
            prefix = "003";
            break;
        default:
            prefix = "999";
    }
    
    // Generate random 6-digit number
    int random = (int) (Math.random() * 1000000);
    return prefix + String.format("%06d", random);
}

