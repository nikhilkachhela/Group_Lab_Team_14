    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String firstName = txtFirstName.getText().trim();
    String lastName = txtLastName.getText().trim();
    String email = txtEmail.getText().trim();
    String username = txtUsername.getText().trim();
    String roleStr = (String) cmbRole.getSelectedItem();
    
    // Validate inputs
    if (firstName.isEmpty() || lastName.isEmpty() || email.isEmpty() || username.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Please fill in all required fields marked with *",
            "Validation Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Validate email format
    if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        JOptionPane.showMessageDialog(this,
            "Please enter a valid email address",
            "Invalid Email",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Check if username already exists
    if (userDirectory.findUserByUsername(username) != null) {
        JOptionPane.showMessageDialog(this,
            "Username '" + username + "' already exists!\nPlease choose a different username.",
            "Username Taken",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Create password using dialog
    PasswordDialog passwordDialog = new PasswordDialog(
        (Frame) SwingUtilities.getWindowAncestor(this),
        "Set Password for " + username
    );
    String password = passwordDialog.getPassword();
    
    if (password == null) {
        // User cancelled
        return;
    }
    
    // Create Person
    String personId = generatePersonId(roleStr);
    Person person = department.getPersonDirectory().newPerson(personId);
    
    // Create Profile based on role
    Object profile = null;
    Role role = Role.valueOf(roleStr);
    
    switch (role) {
        case STUDENT:
            profile = department.getStudentDirectory().newStudentProfile(person);
            break;
        case FACULTY:
            FacultyDirectory facultyDir = new FacultyDirectory(department);
            profile = facultyDir.newFacultyProfile(person);
            break;
        case ADMIN:
        case REGISTRAR:
            profile = person; // For admin/registrar, just use person
            break;
    }
    
    // Create User Account
    User user = userDirectory.newUser(person, username, password, role);
    if (user != null) {
        user.setProfileReference(profile);
        
        JOptionPane.showMessageDialog(this,
            "<html><b>Account Created Successfully!</b><br><br>" +
            "Name: " + firstName + " " + lastName + "<br>" +
            "Username: " + username + "<br>" +
            "Role: " + role.getDisplayName() + "<br>" +
            "Person ID: " + personId + "</html>",
            "Success",
            JOptionPane.INFORMATION_MESSAGE);
        
        // Reset form
        btnResetActionPerformed(null);
    } else {
        JOptionPane.showMessageDialog(this,
            "Failed to create user account. Please try again.",
            "Error",
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCreateActionPerformed

